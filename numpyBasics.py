#numpy basics
import numpy as np
x=np.array([1,2,3])
y=np.array([[1,2],[3,4]])
z=np.array([[1,2,3],[1,2,3]],[[5,5,6],[9,8,9]])
print(x)
print(y)
print(z)
print(np.zeros((3,4)))
print(np.ones((6,4)))
print(np.arrange(0,10,3))
arr1d=np.array([10,22,3,4,5,6,5])
arr2d=np.array([[1,2,3],[2,3,5],[2,4,6]])
print(arr1d)
print(arr2d)
print(arr1d[1:3])
print(arr2d[1:,1])
print(np.absolute(arr1d))
print(np.add(arr1d,arr2d))
print(np.array([0,np.pi/2,np.pi]))
print(np.sin(np.pi/2))
print(np.exp(5))
print(np.sqrt(89))